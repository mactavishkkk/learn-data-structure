public class Operators {
    public static void main(String[] args) {
        Float num = 10.0f;

        // Soma
        System.out.println(num + 2);

        // Subtração
        System.out.println(num - 2);

        // Multiplicação
        System.out.println(num * 2);

        // Divisão
        System.out.println(num / 2);

        // Resto
        System.out.println(num % 3);

        System.out.println(++num);
    }
}
